def sum_num(matrix,n):
    sum_top = 0
    sum_left = 0
    sum_right = 0
    sum_bottom = 0
    for i in range(n):
        for j in range(n):
            if i < j and i < n-1-j :
                sum_top += matrix[i][j]
            elif i < j and i > n - 1 - j:
                sum_right += matrix[i][j]
            elif i > j and i > n-1-j:
                sum_bottom += matrix[i][j]
            elif i > j and i < n-1-j:
                sum_left +=  matrix[i][j]
    return sum_top,sum_right,sum_bottom,sum_left

