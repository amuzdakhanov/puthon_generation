def print_matrix(matrix, n):
    for r in range(n):
        for c in range(n):
            print(matrix[r][c], end=' ')
        print()

n = int(input())
matrix = []
for i in range(n):
    temp = [int(num) for num in input().split()]
    matrix.append(temp)



for i in range(n):
    for j in range(n):
        if i == j:
            tmp = matrix[i][j]
            matrix[i][j] = matrix[n-j-1][j]
            matrix[n - j - 1][j] = tmp



print_matrix(matrix, n)
